    private void showDatabase(){
        try{
            KhachHgDAO dao = new KhachHgDAO();
            List<KhachHgDTO> list = dao.timKHAll();
            model.setRowCount(0);
            for(KhachHgDTO it : list){
                model.addRow(new Object[]{
                    it.getMaKH(),it.getTenKH(),it.getSDT(),it.getDiaChi(),it.getNgaySinh(),it.getNoiSinh(),it.getGioiTinh()==1?"Nam":"Nữ",it.getEmail()
                });
            }
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }

