    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            Makhtxt.setEnabled(false);
            int row = jTable1.getSelectedRow();
            if(row >=0){
                String id = (String)jTable1.getValueAt(row, 0);
                KhachHgDAO dao = new KhachHgDAO();
                KhachHgDTO kh = dao.timKH(id);
                if(kh != null){
                    Makhtxt.setText(kh.getMaKH());
                    Tenkhtxt.setText(kh.getTenKH());
                    Sdttxt.setText(kh.getSDT());
                    Diachitxt.setText(kh.getDiaChi());
                    rbtNam.setSelected(kh.getGioiTinh()==1?true:false);
                    rbtNu.setSelected(kh.getGioiTinh()==0?true:false);
                    Noisinhtxt.setText(kh.getNoiSinh());
                    Namsinhtxt.setText(formatdate.format(kh.getNgaySinh()));
                    Emailtxt.setText(kh.getEmail());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jTable1MouseClicked

