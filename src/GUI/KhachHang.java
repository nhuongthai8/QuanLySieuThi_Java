    private void SuabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuabtnActionPerformed
        StringBuilder sb = new StringBuilder();
        MiscData.DataEmty(Makhtxt, sb, "Vui lòng nhập Mã khách hàng");
        if(sb.length()>0){
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        if(MiscDialog.showConfirmDialog(null, "Bạn có muốn cập nhật lại không?", "Thông báo") == JOptionPane.NO_OPTION){
            return;
        }
        try{
            KhachHgDTO kh = new KhachHgDTO();
            kh.setMaKH(Makhtxt.getText());
            kh.setTenKH(Tenkhtxt.getText());
            kh.setSDT(Sdttxt.getText());
            kh.setDiaChi(Diachitxt.getText());
            kh.setGioiTinh(rbtNam.isSelected()?1:0);
            kh.setNoiSinh(Noisinhtxt.getText());
            kh.setNgaySinh(formatdate.parse(Namsinhtxt.getText()));
            kh.setEmail(Emailtxt.getText());

            KhachHgDAO khdao = new KhachHgDAO();
            if(khdao.updateKH(kh)){
                MiscDialog.showMessageDialog(null, "Chỉnh sửa thành công", "Thông báo");
                showDatabase();
                refresh();
            }else{
                MiscDialog.showErrorDialog(null, "Chỉnh sửa không thành công", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_SuabtnActionPerformed

    private void ThembtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThembtnActionPerformed
        StringBuilder sb = new StringBuilder();
        MiscData.DataEmty(Makhtxt, sb, "Vui lòng nhập Mã khách hàng");
        MiscData.DataEmty(Tenkhtxt, sb, "Vui lòng nhập Tên khách hàng");
        MiscData.DataEmty(Sdttxt, sb, "Vui lòng nhập SĐT");
        MiscData.DataEmty(Diachitxt, sb, "Vui lòng nhập Địa chỉ");
        MiscData.DataEmty(Noisinhtxt, sb, "Vui lòng nhập Nơi sinh");
        MiscData.DataEmty(Namsinhtxt, sb, "Vui lòng nhập Ngày sinh");
        MiscData.DataEmty(Emailtxt, sb, "Vui lòng nhập Email");
        if(sb.length()>0){
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        try{

            KhachHgDTO kh = new KhachHgDTO();
            kh.setMaKH(Makhtxt.getText());
            kh.setTenKH(Tenkhtxt.getText());
            kh.setSDT(Sdttxt.getText());
            kh.setDiaChi(Diachitxt.getText());
            kh.setNgaySinh(formatdate.parse(Namsinhtxt.getText()));
            kh.setNoiSinh(Noisinhtxt.getText());
            kh.setGioiTinh(rbtNam.isSelected()?1:0);
            kh.setEmail(Emailtxt.getText());

            KhachHgDAO dao = new KhachHgDAO();
            if(dao.insertKH(kh)){
                MiscDialog.showMessageDialog(null, "Thêm khách hàng thành công", "Thông báo");
                showDatabase();
                refresh();
            }
            else{
                MiscDialog.showConfirmDialog(null, "Thêm không thành công", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
        }
    }//GEN-LAST:event_ThembtnActionPerformed

    private void XoabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoabtnActionPerformed
        StringBuilder sb = new StringBuilder();
        MiscData.DataEmty(Makhtxt, sb, "Vui lòng nhập Mã khách hàng");
        if(sb.length()>0){
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        if(MiscDialog.showConfirmDialog(null, "Bạn có muốn xóa Khách hàng?", "Thông báo") == JOptionPane.NO_OPTION){
            return;
        }
        try{
            KhachHgDTO kh = new KhachHgDTO();
            kh.setMaKH(Makhtxt.getText());
            KhachHgDAO khdao = new KhachHgDAO();
            if(khdao.deleteKH(kh)){
                MiscDialog.showMessageDialog(null, "Xóa thành công", "Thông báo");
                showDatabase();
                refresh();
            }else{
                MiscDialog.showErrorDialog(null, "Xóa không thành công", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_XoabtnActionPerformed

