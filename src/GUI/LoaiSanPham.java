    private void showTable(){
        model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"MÃ LOẠI SẢN PHẨM","TÊN LOẠI SẢN PHẨM"});
        jTable1.setModel(model);
    }
    private void showDatabase(){
        try{
            LoaiSPDAO dao = new LoaiSPDAO();
            List<LoaiSPDTO> list = dao.timLSPAll();
            model.setRowCount(0);
            for(LoaiSPDTO it : list){
                model.addRow(new Object[]{
                    it.getMaLoai(),it.getTenLoai()
                });
            }
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }
    private void refresh(){
        Malsptxt.setText("");
        Tenlsptxt.setText("");
    }
    
