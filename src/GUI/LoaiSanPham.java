    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            int row = jTable1.getSelectedRow();
            if(row >=0){
                String id = (String)jTable1.getValueAt(row, 0);
                LoaiSPDAO dao = new LoaiSPDAO();
                LoaiSPDTO ncc = dao.timLSP(id);
                if(ncc != null){
                    Malsptxt.setText(ncc.getMaLoai());
                    Tenlsptxt.setText(ncc.getTenLoai());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jTable1MouseClicked

