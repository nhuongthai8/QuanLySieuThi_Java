    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            int row = jTable1.getSelectedRow();
            if(row >=0){
                int id = (int)jTable1.getValueAt(row, 0);
                LoginDAO dao = new LoginDAO();
                LoginDTO lg = dao.timTDN(String.valueOf(id));
                if(lg != null){
                    Idtxt.setText(String.valueOf(lg.getId()));
                    Taikhoantxt.setText(lg.getTenDangNhap());
                    Matkhautxt.setText(lg.getMatKhau());
                    Chucvucb.setSelectedItem(lg.getChucVu());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void ThembtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThembtnActionPerformed
        StringBuilder sb = new StringBuilder();
        MiscData.DataEmty(Taikhoantxt, sb, "Vui lòng nhập Tài khoản");
        MiscData.DataEmty(Matkhautxt, sb, "Vui lòng nhập Mật khẩu");
        if(sb.length()>0){
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        try{
            LoginDTO lg = new LoginDTO();
            lg.setTenDangNhap(Taikhoantxt.getText());
            lg.setMatKhau(Matkhautxt.getText());
            lg.setChucVu(Chucvucb.getSelectedItem().toString());

            LoginDAO dao = new LoginDAO();
            if(dao.insertTK(lg)){
                MiscDialog.showMessageDialog(null, "Thêm Tài khoản thành công", "Thông báo");
                showDatabase();
                refresh();
            }
            else{
                MiscDialog.showErrorDialog(null, "Thêm không thành công", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
        }
    }//GEN-LAST:event_ThembtnActionPerformed

    private void XoabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoabtnActionPerformed
        StringBuilder sb = new StringBuilder();
        MiscData.DataEmty(Idtxt, sb, "Vui lòng nhập Id");
        if(sb.length()>0){
            MiscDialog.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        if(MiscDialog.showConfirmDialog(null, "Bạn có muốn xóa Tài khoản này không?", "Thông báo") == JOptionPane.NO_OPTION){
            return;
        }
        try{
            LoginDTO lg = new LoginDTO();
            lg.setId(Integer.valueOf(Idtxt.getText()));
            LoginDAO nvdao = new LoginDAO();
            if(nvdao.deleteTK(lg)){
                MiscDialog.showMessageDialog(null, "Xóa thành công", "Thông báo");
                showDatabase();
                refresh();
            }else{
                MiscDialog.showErrorDialog(null, "Xóa không thành công", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_XoabtnActionPerformed

