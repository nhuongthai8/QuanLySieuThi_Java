    private void showDatabase(){
        try{
            LoginDAO dao = new LoginDAO();
            List<LoginDTO> list = dao.timTDNAll();
            model.setRowCount(0);
            for(LoginDTO it : list){
                model.addRow(new Object[]{
                    it.getId(),it.getTenDangNhap(),it.getMatKhau(),it.getChucVu()
                });
            }
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MiscDialog.showErrorDialog(null, e.getMessage(), "Lỗi");
        }
    }
    
